
CREATE TABLE cars (cars_ID INT NOT NULL AUTO_INCREMENT,
PRIMARY KEY (cars_ID),
VIN VARCHAR(50) NOT NULL,
Manufacturer VARCHAR(50) NOT NULL,
 Model VARCHAR(50) NOT NULL,
 Year VARCHAR(4) NOT NULL,
 Color VARCHAR(50) NOT NULL)
 ;

CREATE TABLE customers (cust_ID INT NOT NULL AUTO_INCREMENT,
PRIMARY KEY (cust_ID),
Customer_ID INT NOT NULL,
Name VARCHAR(50) NOT NULL,
 Phone_Number VARCHAR(20) NOT NULL,
 Email VARCHAR(50) NOT NULL,
 Address VARCHAR(100) NOT NULL,
 City VARCHAR(50) NOT NULL,
 State_Province VARCHAR(20) NOT NULL,
 Country VARCHAR(45) NOT NULL,
 ZIP_PostalCode VARCHAR(10) NOT NULL)
 ;

CREATE TABLE salesperson (saler_ID INT NOT NULL AUTO_INCREMENT,
PRIMARY KEY (sales_ID),
Staff_ID VARCHAR(5) NOT NULL,
Name VARCHAR(50) NOT NULL,
 Store VARCHAR(50) NOT NULL)
 ;

CREATE TABLE invoices (invoice_ID INT NOT NULL AUTO_INCREMENT,
PRIMARY KEY (invoice_ID),
InvoiceNumber VARCHAR(10) NOT NULL,
Date DATE NOT NULL,
Car INT NOT NULL,
Customer INT NOT NULL,
Salesperson INT NOT NULL);
 ;